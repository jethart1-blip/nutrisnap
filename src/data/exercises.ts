export function getExercisesBySlot(slot: MuscleGroupSlot): ExerciseDefinition[] {
  return EXERCISE_LIBRARY.filter((e) => e.slot === slot || e.secondarySlots?.includes(slot));
}

export function getExercisesByEquipment(equipment: EquipmentType[]): ExerciseDefinition[] {
  return EXERCISE_LIBRARY.filter((e) => e.equipment.some((eq) => equipment.includes(eq)));
}

export function getExerciseById(id: string): ExerciseDefinition | undefined {
  return EXERCISE_LIBRARY.find((e) => e.id === id);
}
